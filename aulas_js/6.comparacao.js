/*
Operadores de comparação

< menor que
<= menor ou igual
> maior que
>= maior ou igual

== igualdade (só valores)
=== igualdade estrita (valores e tipo dos valores)
!= desigualdade
!== desigualdade estrita 
*/

console.log(10 == '10'); // verdadeiro
console.log(10 === '10'); // falso

console.log(10 != '10'); // falso
console.log(10 !== '10'); // verdadeiro



